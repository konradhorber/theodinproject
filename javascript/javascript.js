function add7(number) {
  return number + 7;
}

function multiply(number_one, number_two) {
  return number_one * number_two;
}

function capitalize(string) {
  return string.charAt(0).toUpperCase() + string.slice(1);
}

function lastLetter(string) {
  return string.at(-1);
}

